#include "3-calc.h"

/**
 * op_add - Function that does a sum
 * @a: number 1 to sum
 * @b: number 2 to sum
 * Return: sum of two numbers
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Function that does a substraction
 * @a: number 1
 * @b: number 2
 * Return: substraction of two numbers
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Function that does a multiplication
 * @a: number 1
 * @b: number 2
 * Return: Multiplication of two numbers
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Function that does a division
 * @a: number 1
 * @b: number 2
 * Return: Division of two numbers
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Function that does a module
 * @a: number 1
 * @b: number 2
 * Return: Module of two numbers
 */

int op_mod(int a, int b)
{
	return (a % b);
}
